package basis;

public class string_0_1 {
	public static void main(String args[]) {
		for (int i =0; i<2;i++) {
			for (int j =0; j<2;j++) {
				for (int k =0; k<2;k++) {
					for (int m =0; m<2;m++) {
						for (int n =0; n<2;n++) {
							System.out.print(i);
							System.out.print(j);
							System.out.print(k);
							System.out.print(m);
							System.out.print(n+"\n");
						}
					}
				}
			}
		}
	}
}
